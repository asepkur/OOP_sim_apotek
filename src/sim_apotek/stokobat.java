    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // menampilkan data kedalam form pengisian:
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String id =jTable1.getValueAt(baris, 0).toString();
        jTextField1.setText(id);
        String nama = jTable1.getValueAt(baris,1).toString();
        jTextField2.setText(nama);
        String jenis = jTable1.getValueAt(baris, 2).toString();
        jComboBox1.setSelectedItem(jenis);
        String gol = jTable1.getValueAt(baris, 3).toString();
        jComboBox2.setSelectedItem(gol);
        String harga=jTable1.getValueAt(baris, 4).toString();
        jTextField5.setText(harga);
        String tersed = jTable1.getValueAt(baris, 5).toString();
        jTextField6.setText(tersed);
    }//GEN-LAST:event_jTable1MouseClicked

